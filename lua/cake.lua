---@param config CakeConfig?
Cake.setup = function(config)
  Cake.config = vim.tbl_deep_extend("force", Cake.config, config or {})
end

--- main load function
Cake.load = function()
  if vim.version().minor < 8 then
    vim.notify_once("cake.nvim: you must use neovim 0.8 or higher")
    return
  end

  -- reset colors
  if vim.g.colors_name then
    vim.cmd.hi("clear")
  end
  vim.g.colors_name = "cake"
  vim.o.termguicolors = true

  local groups = get_groups()

  -- add highlights
  for group, settings in pairs(groups) do
    vim.api.nvim_set_hl(0, group, settings)
  end
end

return Cake
